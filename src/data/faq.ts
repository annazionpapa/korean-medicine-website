/** FAQ에서 프로그램별로 관련 질문을 찾는 유틸 */
export function getFaqsByProgram(programId: string): FAQ[] {
  const programFaqMap: Record<string, string[]> = {
    pain: ["f5", "f6", "f7", "f9"],
    traffic: ["f11", "f7", "f8"],
    internal: ["f7", "f8", "f14"],
    women: ["f14", "f15", "f7"],
    diet: ["f17", "f15", "f14"],
    skin: ["f7", "f14", "f9"],
  };
  const ids = programFaqMap[programId] || [];
  return faqs.filter((f) => ids.includes(f.id)).slice(0, 3);
}
